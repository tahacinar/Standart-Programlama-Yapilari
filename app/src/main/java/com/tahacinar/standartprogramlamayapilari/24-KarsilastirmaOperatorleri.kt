package com.tahacinar.standartprogramlamayapilari

fun main () {

    var s1=60
    var s2=50

    var y1=70
    var y2=60

    println(s1 == s2) // Burada == ile eşitlik kontrolü yapıyoruz. Çıktımız False olacaktır.

    println(s1 != s2) // Burada != ile eşit değil kontrolü yapıyoruz. Ve sonucumuz True olacaktır.

    println(s1 > s2)  // Burada > büyüklük kontrolü yapıyoruz. Ve sonucumuz True olacaktır.

    println(s1 >= s2) // Burada >= büyük eşit kontrolü yapıyoruz. Ve sonucumuz True olacaktır.

    println(s1 < s2)  // Burada < küçüklük kontrolü yapıyoruz. Ve sonucumuz False olacaktır.

    println(s1 <= s2) // Burada <= küçük eşit kontrolü yapıyoruz. Ve sonucumuz False olacaktır.


    // Mantıksal Olanlar

    println(s1 > s2 || y1 > y2) // Burada 2 koşuldan biri sağlanıyorsa cevap True olur

    println(s1 > s2 && y1 > y2) // Burada 2 koşulda sağlanırsa cevap True olur





}